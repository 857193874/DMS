package bean;

public class Power {
	private String powerId;
	private String totalconsumption;
	private String subsidyconsumption;
	private String payconsumption;
	private String freeconsumption;
	private String price;
	private String powerBuildingId;
	public String getPowerId() {
		return powerId;
	}
	public void setPowerId(String powerId) {
		this.powerId = powerId;
	}
	public String getTotalconsumption() {
		return totalconsumption;
	}
	public void setTotalconsumption(String totalconsumption) {
		this.totalconsumption = totalconsumption;
	}
	public String getSubsidyconsumption() {
		return subsidyconsumption;
	}
	public void setSubsidyconsumption(String subsidyconsumption) {
		this.subsidyconsumption = subsidyconsumption;
	}
	public String getPayconsumption() {
		return payconsumption;
	}
	public void setPayconsumption(String payconsumption) {
		this.payconsumption = payconsumption;
	}
	public String getFreeconsumption() {
		return freeconsumption;
	}
	public void setFreeconsumption(String freeconsumption) {
		this.freeconsumption = freeconsumption;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getPowerBuildingId() {
		return powerBuildingId;
	}
	public void setPowerBuildingId(String powerBuildingId) {
		this.powerBuildingId = powerBuildingId;
	}
	
}
